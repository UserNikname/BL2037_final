def exp_vs_af_similarity_profile(exp_chain, af_chain, window: int = 15) -> pd.DataFrame:
    """Return a per-residue similarity profile on experimental residue numbering.

    Output columns:
    - exp_resseq, exp_aa
    - af_resseq, af_aa (NaN if gap)
    - is_aligned (bool)
    - is_match (bool/NaN)
    - rolling_identity_pct (float/NaN)
    """
    exp_seq, exp_res = chain_sequence_and_residues(exp_chain)
    af_seq, af_res = chain_sequence_and_residues(af_chain)

    pairs = align_index_pairs(exp_seq, af_seq)
    exp_to_af: Dict[int, int] = {i: j for i, j in pairs}

    rows: List[Dict] = []
    for exp_i, exp_r in enumerate(exp_res):
        exp_resseq = int(exp_r.id[1])
        exp_aa = seq1(exp_r.get_resname())
        if exp_i in exp_to_af:
            af_i = exp_to_af[exp_i]
            af_r = af_res[af_i]
            af_resseq = int(af_r.id[1])
            af_aa = seq1(af_r.get_resname())
            is_match = bool(exp_aa == af_aa)
            rows.append({
                "exp_resseq": exp_resseq,
                "exp_aa": exp_aa,
                "af_resseq": af_resseq,
                "af_aa": af_aa,
                "is_aligned": True,
                "is_match": is_match,
            })
        else:
            rows.append({
                "exp_resseq": exp_resseq,
                "exp_aa": exp_aa,
                "af_resseq": np.nan,
                "af_aa": "-",
                "is_aligned": False,
                "is_match": np.nan,
            })

    df = pd.DataFrame(rows)
    df["rolling_identity_pct"] = (
        df["is_match"].astype(float)
        .rolling(window=window, center=True, min_periods=max(3, window // 3))
        .mean()
        * 100.0
    )
    return df

