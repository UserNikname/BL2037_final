# Phase B: Sequence analysis (pairwise identity matrices)

def compute_identity_matrix(labels: List[str], seqs: List[str]) -> pd.DataFrame:
    """Pairwise identity matrix using aligned-only identity (matches / aligned pairs)."""
    n = len(seqs)
    mat = np.zeros((n, n), dtype=float)
    for i in range(n):
        for j in range(n):
            mat[i, j] = aligned_identity(seqs[i], seqs[j])
    return pd.DataFrame(mat, index=labels, columns=labels)

def identity_over_maxlen_and_coverage_matrix(labels: List[str], seqs: List[str]) -> Tuple[pd.DataFrame, pd.DataFrame]:
    """Return (identity_over_maxlen_pct, aligned_coverage_pct) matrices.

    - identity_over_maxlen_pct = matches / max(lenA, lenB) * 100
    - aligned_coverage_pct = aligned_pairs / max(lenA, lenB) * 100
    """
    n = len(seqs)
    id_over_maxlen = np.zeros((n, n), dtype=float)
    coverage = np.zeros((n, n), dtype=float)
    for i in range(n):
        for j in range(n):
            a, b = seqs[i], seqs[j]
            pairs = align_index_pairs(a, b)
            if not pairs:
                continue
            matches = sum(1 for ia, jb in pairs if a[ia] == b[jb])
            denom_maxlen = max(len(a), len(b))
            id_over_maxlen[i, j] = 100.0 * matches / denom_maxlen
            coverage[i, j] = 100.0 * len(pairs) / denom_maxlen
    return (
        pd.DataFrame(id_over_maxlen, index=labels, columns=labels),
        pd.DataFrame(coverage, index=labels, columns=labels),
    )

