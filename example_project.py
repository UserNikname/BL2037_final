def get_contact_matrix(chain) -> np.ndarray:
    """Calculate the distance matrix between all CA atoms in a chain."""
    # Extract list of CA residues using your existing helper function
    _, residues = chain_sequence_and_residues(chain)

    n = len(residues)
    matrix = np.zeros((n, n))

    # Calculate pairwise distances
    # Note: Bio.PDB 'minus' operator on atoms returns distance
    for i in range(n):
        for j in range(n):
            dist = residues[i]["CA"] - residues[j]["CA"]
            matrix[i, j] = dist

    return matrix

