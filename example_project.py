# Identity diagnostics (kept minimal)
def _offdiag_vals(df: pd.DataFrame) -> np.ndarray:
    """Return the off-diagonal values of a square matrix DataFrame as a 1D array."""
    v = df.values.astype(float)
    mask = ~np.eye(v.shape[0], dtype=bool)
    return v[mask]

def _print_stats(name: str, df: pd.DataFrame) -> None:
    """Print min/median/mean/max for off-diagonal identity values."""
    vals = _offdiag_vals(df)
    if len(vals) == 0:
        print(f"{name}: no off-diagonal values")
        return
    print(
        f"{name}: off-diagonal min/median/mean/max = "
        f"{np.min(vals):.1f} / {np.median(vals):.1f} / {np.mean(vals):.1f} / {np.max(vals):.1f}"
    )

