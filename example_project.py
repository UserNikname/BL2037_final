# Phase C extra: segment the "bad" regions from per-residue deviation

def find_bad_segments(
    per_res_df: pd.DataFrame, threshold_A: float = 4.0, min_len: int = 5
 ) -> pd.DataFrame:
    """Find contiguous segments where ca_distance_A >= threshold_A.

    Returns columns: start_resseq, end_resseq, length, mean_ca_A, max_ca_A
    """
    df = per_res_df[["exp_resseq", "ca_distance_A"]].sort_values("exp_resseq").reset_index(drop=True)
    bad = df["ca_distance_A"] >= float(threshold_A)

    segments: List[Dict] = []
    start_idx: int | None = None
    for i, is_bad in enumerate(bad.tolist()):
        if is_bad and start_idx is None:
            start_idx = i
        elif (not is_bad) and start_idx is not None:
            seg = df.iloc[start_idx:i]
            if len(seg) >= min_len:
                segments.append({
                    "start_resseq": int(seg["exp_resseq"].iloc[0]),
                    "end_resseq": int(seg["exp_resseq"].iloc[-1]),
                    "length": int(len(seg)),
                    "mean_ca_A": float(seg["ca_distance_A"].mean()),
                    "max_ca_A": float(seg["ca_distance_A"].max()),
                })
            start_idx = None

    # handle segment reaching end
    if start_idx is not None:
        seg = df.iloc[start_idx:]
        if len(seg) >= min_len:
            segments.append({
                "start_resseq": int(seg["exp_resseq"].iloc[0]),
                "end_resseq": int(seg["exp_resseq"].iloc[-1]),
                "length": int(len(seg)),
                "mean_ca_A": float(seg["ca_distance_A"].mean()),
                "max_ca_A": float(seg["ca_distance_A"].max()),
            })

    return pd.DataFrame(segments)


