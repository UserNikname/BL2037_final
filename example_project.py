def is_water_residue(res) -> bool:
    """Return True if a residue looks like crystallographic water."""
    return res.get_resname().upper() in {"HOH", "WAT", "H2O"}

def iter_ligand_residues(model):
    """Yield non-water hetero residues (possible ligands/ions)."""
    for chain in model.get_chains():
        for res in chain.get_residues():
            hetfield = str(res.id[0]).strip()
            if hetfield == "":
                continue  # standard protein residue
            if is_water_residue(res):
                continue
            yield chain.id, res

def ligand_contacts_for_chain(exp_structure, protein_chain_id: str, cutoff_A: float = 4.0) -> pd.DataFrame:
    """Compute ligand contacts for one protein chain using a distance cutoff."""
    model = next(exp_structure.get_models())
    protein_chain = model[protein_chain_id]

    protein_atoms = [a for a in protein_chain.get_atoms() if a.element != "H"]
    ns = NeighborSearch(protein_atoms)

    rows: List[Dict] = []
    for lig_chain_id, lig_res in iter_ligand_residues(model):
        lig_atoms = [a for a in lig_res.get_atoms() if a.element != "H"]
        for atom in lig_atoms:
            close_atoms = ns.search(atom.get_coord(), cutoff_A, level="A")
            for close_atom in close_atoms:
                close_res = close_atom.get_parent()
                rows.append(
                    {
                        "lig_chain": lig_chain_id,
                        "lig_resname": lig_res.get_resname(),
                        "lig_resseq": int(lig_res.id[1]),
                        "protein_chain": protein_chain_id,
                        "prot_resname": close_res.get_resname(),
                        "prot_resseq": int(close_res.id[1]),
                        "distance_A": float(np.linalg.norm(atom.get_coord() - close_atom.get_coord())),
                    }
                )
    if not rows:
        return pd.DataFrame(
            columns=["lig_chain", "lig_resname", "lig_resseq", "protein_chain", "prot_resname", "prot_resseq", "distance_A"]
        )
    return pd.DataFrame(rows)

phaseD_summary_rows = []
for _, row in phaseA_df.iterrows():
    pdb_id = row["pdb_id"]
    chain_id = row["chain_id"]
    uniprot_id = row["uniprot_id"]

    exp_struct = parse_structure(Path(row["exp_file"]), f"EXP_{pdb_id}")
    contacts_df = ligand_contacts_for_chain(exp_struct, chain_id, cutoff_A=4.0)

    out_csv = RESULTS_DIR / f"contacts_{pdb_id}_{chain_id}.csv"
    contacts_df.to_csv(out_csv, index=False)

    # summarize top contacting residues (by number of contacts)
    if len(contacts_df) > 0:
        top = (
            contacts_df.groupby(["prot_resseq", "prot_resname"])
            .size()
            .sort_values(ascending=False)
            .head(12)
        )
        fig, ax = plt.subplots(figsize=(7, 3))
        labels = [f"{r}:{n}" for (r, n) in top.index]
        ax.bar(range(len(top)), top.values)
        ax.set_xticks(range(len(top)))
        ax.set_xticklabels(labels, rotation=45, ha="right")
        ax.set_ylabel("# atom-atom contacts")
        ax.set_title(f"Top ligand-contact residues ({pdb_id}:{chain_id})")
        fig.tight_layout()
        out_png = CONTACTS_DIR / f"contacts_{pdb_id}_{chain_id}.png"
        fig.savefig(out_png)
        plt.close(fig)
        n_ligands = contacts_df[["lig_chain", "lig_resname", "lig_resseq"]].drop_duplicates().shape[0]
    else:
        out_png = ""
        n_ligands = 0

    phaseD_summary_rows.append(
        {
            "pdb_id": pdb_id,
            "chain_id": chain_id,
            "uniprot_id": uniprot_id,
            "n_contacts": int(len(contacts_df)),
            "n_ligands": int(n_ligands),
            "contacts_csv": str(out_csv),
            "plot_png": str(out_png),
        }
    )
    print(f"Phase D done: {pdb_id}:{chain_id} | ligands={n_ligands} | contacts={len(contacts_df)}")

phaseD_df = pd.DataFrame(phaseD_summary_rows)
phaseD_df.to_csv(RESULTS_DIR / "phaseD_summary.csv", index=False)
