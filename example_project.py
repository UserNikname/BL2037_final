def fetch_uniprot_sequence(uniprot_id: str) -> str:
    """Fetch the canonical UniProt sequence via SwissProt."""
    handle = ExPASy.get_sprot_raw(uniprot_id)
    record = SwissProt.read(handle)
    return record.sequence


def download_file(url: str, out_path: Path, timeout: int = 60) -> Path:
    """Download a URL to disk (skips if the file already exists)."""
    out_path.parent.mkdir(parents=True, exist_ok=True)
    if out_path.exists() and out_path.stat().st_size > 0:
        return out_path
    r = requests.get(url, timeout=timeout)
    r.raise_for_status()
    out_path.write_bytes(r.content)
    return out_path


def download_rcsb_mmcif(pdb_id: str) -> Path:
    """Download an RCSB mmCIF file for a PDB ID."""
    pdb_id = pdb_id.upper()
    url = f"https://files.rcsb.org/download/{pdb_id}.cif"
    out_path = DOWNLOADS_DIR / f"{pdb_id}.cif"
    return download_file(url, out_path)


def download_alphafold_pdb(uniprot_id: str) -> Path:
    """Download the latest AlphaFold model for a UniProt accession.

    Uses the AlphaFold API first (recommended), then falls back to common versioned filenames.
    Returns a local path (usually .pdb or .cif).
    """
    uniprot_id = uniprot_id.upper()

    # Reuse any already-downloaded model first (prefer PDB).
    already = (
        sorted(DOWNLOADS_DIR.glob(f"AF-{uniprot_id}-F1-model_v*.pdb"), reverse=True)
        + sorted(DOWNLOADS_DIR.glob(f"AF-{uniprot_id}-F1-model_v*.cif"), reverse=True)
        + [DOWNLOADS_DIR / f"AF-{uniprot_id}.pdb"]
    )
    for p in already:
        if p.exists() and p.stat().st_size > 0:
            return p

    api_url = f"https://alphafold.ebi.ac.uk/api/prediction/{uniprot_id}"
    last_err = None
    try:
        r = requests.get(api_url, timeout=30)
        r.raise_for_status()
        data = r.json()
        if isinstance(data, list) and len(data) > 0:
            rec = data[0]
            model_url = rec.get("pdbUrl") or rec.get("cifUrl") or rec.get("bcifUrl")
            if model_url:
                out_path = DOWNLOADS_DIR / Path(model_url).name
                return download_file(model_url, out_path)
    except Exception as e:
        last_err = e

    # Fallback: try a range of versions if the API is unavailable.
    versions = [f"v{i}" for i in range(6, 0, -1)]
    for v in versions:
        for ext in ("pdb", "cif"):
            url = f"https://alphafold.ebi.ac.uk/files/AF-{uniprot_id}-F1-model_{v}.{ext}"
            out_path = DOWNLOADS_DIR / f"AF-{uniprot_id}-F1-model_{v}.{ext}"
            try:
                return download_file(url, out_path)
            except Exception as e:
                last_err = e
                continue

    raise RuntimeError(
        f"Could not download AlphaFold model for {uniprot_id}. "
        f"Tried API ({api_url}) and common versioned filenames. Last error: {last_err}"
    )


def parse_structure(path: Path, structure_id: str):
    """Parse a mmCIF or PDB file into a Biopython Structure."""
    suffix = path.suffix.lower()
    if suffix == ".cif":
        parser = MMCIFParser(QUIET=True)
        return parser.get_structure(structure_id, str(path))
    if suffix == ".pdb":
        parser = PDBParser(QUIET=True)
        return parser.get_structure(structure_id, str(path))
    raise ValueError(f"Unsupported file type: {path}")


def get_chain(structure, chain_id: str):
    """Return a chain from the first model (raises a helpful error if missing)."""
    model = next(structure.get_models())
    try:
        return model[chain_id]
    except KeyError as e:
        available = [c.id for c in model.get_chains()]
        raise KeyError(f"Chain {chain_id} not found. Available: {available}") from e


def iter_standard_residues_with_ca(chain):
    """Yield standard amino-acid residues that have a CA atom."""
    for res in chain.get_residues():
        if is_aa(res, standard=True) and "CA" in res:
            yield res


def chain_sequence_and_residues(chain) -> Tuple[str, List]:
    """Return (sequence, residues_list) using standard AA residues with CA."""
    residues = list(iter_standard_residues_with_ca(chain))
    seq = "".join(seq1(r.get_resname()) for r in residues)
    return seq, residues


def align_index_pairs(seq_a: str, seq_b: str) -> List[Tuple[int, int]]:
    """Return list of (i, j) index pairs for aligned (non-gap) positions."""
    aligner = PairwiseAligner()
    aligner.mode = "global"
    aligner.match_score = 2
    aligner.mismatch_score = -1
    aligner.open_gap_score = -0.5
    aligner.extend_gap_score = -0.1
    aln = aligner.align(seq_a, seq_b)[0]
    pairs: List[Tuple[int, int]] = []
    # aln.aligned gives matching blocks in A and B
    blocks_a = aln.aligned[0]
    blocks_b = aln.aligned[1]
    for (a0, a1), (b0, b1) in zip(blocks_a, blocks_b):
        length = min(a1 - a0, b1 - b0)
        for k in range(length):
            pairs.append((a0 + k, b0 + k))
    return pairs


def aligned_identity(seq_a: str, seq_b: str) -> float:
    """Percent identity on aligned (non-gap) positions."""
    pairs = align_index_pairs(seq_a, seq_b)
    if not pairs:
        return 0.0
    matches = sum(1 for i, j in pairs if seq_a[i] == seq_b[j])
    return 100.0 * matches / len(pairs)


def superpose_on_ca(exp_chain, af_chain) -> Tuple[float, List[Dict]]:
    """Superpose AF chain onto experimental chain using aligned CA atoms.

    Returns (global_rmsd_A, per_residue_records) where per_residue_records includes
    CA distance after superposition for each aligned residue pair.
    """
    exp_seq, exp_res = chain_sequence_and_residues(exp_chain)
    af_seq, af_res = chain_sequence_and_residues(af_chain)
    pairs = align_index_pairs(exp_seq, af_seq)
    if len(pairs) < 10:
        raise RuntimeError(f"Too few aligned residues ({len(pairs)}). Check chain IDs / sequences.")

    exp_atoms = [exp_res[i]["CA"] for i, j in pairs]
    af_atoms = [af_res[j]["CA"] for i, j in pairs]

    sup = Superimposer()
    sup.set_atoms(exp_atoms, af_atoms)
    sup.apply(list(af_chain.get_atoms()))
    global_rmsd = float(sup.rms)

    records: List[Dict] = []
    for (i, j) in pairs:
        exp_ca = exp_res[i]["CA"].get_coord()
        af_ca = af_res[j]["CA"].get_coord()
        dist = float(np.linalg.norm(exp_ca - af_ca))
        records.append(
            {
                "exp_resseq": int(exp_res[i].id[1]),
                "exp_resname": exp_res[i].get_resname(),
                "af_resseq": int(af_res[j].id[1]),
                "af_resname": af_res[j].get_resname(),
                "ca_distance_A": dist,
            },
        )
    return global_rmsd, records


# The notebook will auto-resolve the correct UniProt accession for each PDB chain
# using RCSB (SIFTS-backed) cross-references, then download the AlphaFold model.

RCSB_GRAPHQL = "https://data.rcsb.org/graphql"
_RCSB_ENTITY_QUERY = """
query($id: String!) {
  entry(entry_id: $id) {
    polymer_entities {
      rcsb_id
      entity_poly { pdbx_strand_id }
      rcsb_polymer_entity_container_identifiers { uniprot_ids }
    }
  }
}
"""


def _rcsb_polymer_entities(pdb_id: str) -> List[Dict]:
    """Query RCSB GraphQL for polymer entities and UniProt cross-references."""
    r = requests.post(
        RCSB_GRAPHQL,
        json={"query": _RCSB_ENTITY_QUERY, "variables": {"id": pdb_id.upper()}},
        timeout=30,
    )
    r.raise_for_status()
    entry = (r.json().get("data") or {}).get("entry")
    if not entry:
        raise ValueError(f"RCSB returned no entry for PDB ID {pdb_id}")
    entities = entry.get("polymer_entities") or []
    out: List[Dict] = []
    for ent in entities:
        chains_raw = ((ent.get("entity_poly") or {}).get("pdbx_strand_id") or "").strip()
        chains = [c.strip() for c in chains_raw.split(",") if c.strip()]
        uniprot_ids = (
            (ent.get("rcsb_polymer_entity_container_identifiers") or {}).get("uniprot_ids")
            or []
        )
        out.append({"entity_id": ent.get("rcsb_id"), "chains": chains, "uniprot_ids": uniprot_ids})
    return out


def resolve_chain_and_uniprot(
    pdb_id: str, preferred_chain: str = "A", strict_chain: bool = False
 ) -> Tuple[str, str, List[str], str]:
    """Resolve a (chain_id, uniprot_id) for a PDB entry.

    - If preferred_chain exists, use it.
    - If it doesn't exist and strict_chain=False, pick the first protein chain that has a UniProt mapping.
    - If multiple UniProt IDs map to the same chain, we pick the first and record a note.
    """
    pdb_id = pdb_id.upper()
    preferred_chain = preferred_chain.strip()
    entities = _rcsb_polymer_entities(pdb_id)

    # 1) Preferred chain if present
    if preferred_chain:
        for ent in entities:
            if preferred_chain in ent["chains"]:
                unis = ent["uniprot_ids"]
                if not unis:
                    raise ValueError(f"{pdb_id} chain {preferred_chain}: found chain but no UniProt mapping in RCSB")
                note = ""
                if len(unis) > 1:
                    note = f"Multiple UniProt IDs for chain {preferred_chain}: {unis} (picked {unis[0]})"
                return preferred_chain, unis[0], unis, note

    if strict_chain:
        all_chains = sorted({c for e in entities for c in e["chains"]})
        raise ValueError(f"{pdb_id}: preferred chain {preferred_chain!r} not found. Available: {all_chains}")

    # 2) Fallback: first entity with a UniProt mapping
    for ent in entities:
        unis = ent["uniprot_ids"]
        if unis and ent["chains"]:
            chosen_chain = ent["chains"][0]
            note = f"Preferred chain {preferred_chain!r} not found; using chain {chosen_chain}"
            if len(unis) > 1:
                note += f" | Multiple UniProt IDs: {unis} (picked {unis[0]})"
            return chosen_chain, unis[0], unis, note

    raise ValueError(f"{pdb_id}: could not find any polymer entity with UniProt mapping")


def build_mapping_table(
    pdb_ids: List[str], preferred_chain: str = "A", strict_chain: bool = False
 ) -> pd.DataFrame:
    """Build a table mapping each PDB ID to a chosen chain and UniProt ID."""
    rows: List[Dict] = []
    for pdb_id in pdb_ids:
        chain_id, uniprot_id, unis, note = resolve_chain_and_uniprot(
            pdb_id, preferred_chain=preferred_chain, strict_chain=strict_chain
        )
        rows.append(
            {
                "pdb_id": pdb_id.upper(),
                "requested_chain": preferred_chain,
                "chosen_chain": chain_id,
                "uniprot_id": uniprot_id,
                "all_uniprot_ids": ",".join(unis),
                "note": note,
            },
        )
    return pd.DataFrame(rows)


def alphafold_latest_model_url(uniprot_id: str) -> str | None:
    """Return the latest AlphaFold model URL for a UniProt ID, or None if not available."""
    api_url = f"https://alphafold.ebi.ac.uk/api/prediction/{uniprot_id.upper()}"
    try:
        r = requests.get(api_url, timeout=20)
        if r.status_code == 404:
            return None
        r.raise_for_status()
        data = r.json()
        if isinstance(data, list) and len(data) > 0:
            rec = data[0]
            return rec.get("pdbUrl") or rec.get("cifUrl") or rec.get("bcifUrl")
    except Exception:
        return None
    return None

